                    <h3>Ingrediënten</h3>
                    <p>Hieronder vind je een recept voor traditionele baklava:

                        Ingrediënten:
                        <br>
                        500 g bloem
                        <br>
                        2 theelepels droge gist
                        <br>
                        1 theelepel zout
                        <br>
                        1 theelepel suiker
                        <br>
                        1/4 kopje olijfolie
                        <br>
                        1 1/4 kopje warm water
                        <br>
                        300 g gehakt (lams-, rund- of kalfsvlees)
                        <br>
                        2 tomaten, fijngehakt
                        <br>
                        1 ui, fijngehakt
                        <br>
                        2 teentjes knoflook, fijngehakt
                        <br>
                        1 rode paprika, fijngehakt
                        <br>
                        2 eetlepels tomatenpuree
                        <br>
                        2 eetlepels paprikapasta (of tomatenpuree)
                        <br>
                        1/4 kopje gehakte peterselie
                        <br>
                        1/4 kopje gehakte munt
                        <br>
                        2 theelepels zout
                        <br>
                        1 theelepel zwarte peper
                        <br>
                        Sap van 1 citroen

                        <br>
                        <br>
                        <br>

                    <h3>Bereidingswijze:</h3>
                    <br>
                    1. Meng de bloem, gist, zout en suiker in een kom. Voeg de olijfolie en warm water toe en kneed het deeg ongeveer 10 minuten tot het glad en elastisch is. Laat het deeg op een warme plaats rijzen, afgedekt met een vochtige doek, tot het verdubbeld is in volume.
                    <br>
                    2. Terwijl het deeg rijst, maak je het beleg. Bak het gehakt in een koekenpan tot het bruin is en laat het afkoelen. Voeg de tomaten, ui, knoflook, paprika, tomatenpuree, paprikapasta, peterselie, munt, zout, peper en citroensap toe aan het gehakt en meng goed.
                    <br>
                    3. Verwarm de oven voor op 220 graden Celsius. Verdeel het deeg in 10 tot 12 stukken en rol elk stuk uit tot een cirkel van ongeveer 20 cm.
                    <br>
                    4. Leg de deegcirkels op een met bakpapier beklede bakplaat en spreid het gehaktmengsel uit over elk stuk deeg, waarbij je ervoor zorgt dat er een beetje rand overblijft.
                    <br>
                    5. Bak de lahmacun in de voorverwarmde oven gedurende ongeveer 10-12 minuten, of tot het deeg goudbruin is en het gehakt gaar is.
                    <br>
                    6. Serveer de lahmacun warm, gegarneerd met verse peterselie en een partje citroen.
                    <br>
                    Geniet van je traditionele Lahmacun!
