                    <h3>Ingrediënten</h3>
                    <p>Hieronder vind je een recept voor traditionele baklava:

                        Ingrediënten:
                        <br>
                        450 gram filodeeg
                        <br>
                        250 gram boter, gesmolten
                        <br>
                        250 gram gemalen walnoten
                        <br>
                        250 gram suiker
                        <br>
                        240 ml water
                        <br>
                        1 theelepel citroensap
                        <br>
                        1 theelepel vanille-extract
                        <br>
                        1 theelepel gemalen kaneel
                        <br>
                        <br>
                        <br>
                        <br>

                    <h3>Bereidingswijze:</h3>
                    <br>
                    1. Verwarm de oven voor op 175 graden Celsius.
                    <br>
                    2. Vet een bakvorm in met boter.
                    <br>
                    3. Leg 10 vellen filodeeg in de bakvorm en bestrijk elk vel met gesmolten boter.
                    <br>
                    4. Meng de gemalen walnoten en kaneel en verdeel dit over het filodeeg.
                    <br>
                    5. Leg hierop weer 5 vellen filodeeg en bestrijk elk vel weer met boter.
                    <br>
                    6. Herhaal dit proces tot alle vellen filodeeg op zijn.
                    <br>
                    7. Snijd de baklava in diamantvormige stukken met een scherp mes.
                    <br>
                    8. Bak de baklava gedurende 45-50 minuten in de oven, of tot deze goudbruin en knapperig is.
                    <br>
                    9. Terwijl de baklava in de oven staat, maak je de siroop: meng het water, suiker, citroensap en vanille-extract in een pan en breng aan de kook. Laat het 10 minuten zachtjes koken tot de suiker is opgelost en de siroop iets dikker is geworden.
                    <br>
                    10. Haal de baklava uit de oven en giet de siroop eroverheen. Zorg dat de siroop goed verdeeld is over de baklava.
                    <br>
                    11. Laat de baklava afkoelen en serveer.
                    <br>
                    Geniet van je traditionele baklava!
